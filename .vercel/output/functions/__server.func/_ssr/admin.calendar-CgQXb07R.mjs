function isoWeekToRange(year, week) {
  const simple = new Date(Date.UTC(year, 0, 4));
  const dayOfWeek = simple.getUTCDay() || 7;
  const isoMon = new Date(simple);
  isoMon.setUTCDate(simple.getUTCDate() - (dayOfWeek - 1) + (week - 1) * 7);
  const sun = new Date(isoMon);
  sun.setUTCDate(isoMon.getUTCDate() - 1);
  const sat = new Date(sun);
  sat.setUTCDate(sun.getUTCDate() + 6);
  return { start: sun, end: sat };
}
function fmtDate(d) {
  return `${String(d.getUTCDate()).padStart(2, "0")}/${String(d.getUTCMonth() + 1).padStart(2, "0")}/${d.getUTCFullYear()}`;
}
function rtlText(pdf, str, xRight, y, maxW) {
  if (!str) return;
  if (maxW) {
    const lines = pdf.splitTextToSize(str, maxW);
    lines.forEach((ln, i) => {
      pdf.text(toVisual(ln), xRight, y + i * (pdf.getLineHeight() / pdf.internal.scaleFactor), {
        align: "right"
      });
    });
  } else {
    pdf.text(toVisual(str), xRight, y, { align: "right" });
  }
}
function drawVisual(pdf, str, x, y, align) {
  pdf.text(toVisual(str), x, y, { align });
}
function wrappedHeight(pdf, str, maxW) {
  if (!str) return 0;
  const lines = pdf.splitTextToSize(str, maxW);
  return lines.length * (pdf.getLineHeight() / pdf.internal.scaleFactor);
}
